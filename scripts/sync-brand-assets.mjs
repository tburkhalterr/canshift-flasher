#!/usr/bin/env node
// scripts/sync-brand-assets.mjs
//
// Sync brand assets from sibling canshift-studio + canshift-core repos into
// this flasher tree:
//   - public/canshift_studio_logo.png  ← canshift-studio/assets/CANShift_studio_logo.png
//   - public/favicon.png               ← canshift-studio/assets/icon.png
//   - src/styles/tokens.css            ← canshift-core/src/design-tokens.ts (DARK_TOKENS.colors)
//
// Manual maintenance script — NOT run in CI (sibling repos are not checked
// out there). Run with `npm run sync-brand` after any studio identity update.

import { copyFileSync, existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(SCRIPT_DIR, '..')
const STUDIO_ROOT = resolve(REPO_ROOT, '..', 'canshift-studio')
const CORE_ROOT = resolve(REPO_ROOT, '..', 'canshift-core')

const STUDIO_LOGO = resolve(STUDIO_ROOT, 'assets', 'CANShift_studio_logo.png')
const STUDIO_ICON = resolve(STUDIO_ROOT, 'assets', 'icon.png')
const CORE_TOKENS = resolve(CORE_ROOT, 'src', 'design-tokens.ts')

const FLASHER_LOGO = resolve(REPO_ROOT, 'public', 'canshift_studio_logo.png')
const FLASHER_FAVICON = resolve(REPO_ROOT, 'public', 'favicon.png')
const FLASHER_TOKENS_CSS = resolve(REPO_ROOT, 'src', 'styles', 'tokens.css')

/** camelCase → kebab-case (used for CSS variable names). Also splits on digits
 * so `surface2` becomes `surface-2` to match the existing tokens.css naming. */
function camelToKebab(key) {
  return key
    .replace(/([a-z])(\d)/g, '$1-$2')
    .replace(/[A-Z]/g, (m) => `-${m.toLowerCase()}`)
}

/** Extract the DARK_TOKENS.colors block from design-tokens.ts as a key → hex map. */
function parseDarkTokensColors(source) {
  const start = source.indexOf('export const DARK_TOKENS')
  if (start === -1) throw new Error('DARK_TOKENS not found in design-tokens.ts')
  const colorsStart = source.indexOf('colors:', start)
  if (colorsStart === -1) throw new Error('DARK_TOKENS.colors not found')
  const open = source.indexOf('{', colorsStart)
  if (open === -1) throw new Error('DARK_TOKENS.colors opening brace not found')

  let depth = 0
  let close = -1
  for (let i = open; i < source.length; i += 1) {
    const ch = source[i]
    if (ch === '{') depth += 1
    else if (ch === '}') {
      depth -= 1
      if (depth === 0) {
        close = i
        break
      }
    }
  }
  if (close === -1) throw new Error('DARK_TOKENS.colors closing brace not found')

  const body = source.slice(open + 1, close)
  const entryRegex = /(\w+)\s*:\s*['"]([^'"]+)['"]/g
  /** @type {Record<string, string>} */
  const out = {}
  let match
  while ((match = entryRegex.exec(body)) !== null) {
    const key = match[1]
    const hex = match[2]
    if (!key || !hex) continue
    out[key] = hex.toLowerCase()
  }
  if (Object.keys(out).length === 0) throw new Error('DARK_TOKENS.colors had no parseable entries')
  return out
}

/** Read tokens.css and return everything before the `:root {` block (preserves @font-face). */
function preservePrelude(cssPath) {
  if (!existsSync(cssPath)) {
    return [
      '/* src/styles/tokens.css',
      ' *',
      ' * Full mirror of canshift-core/src/design-tokens.ts DARK_TOKENS.colors.',
      ' * Regenerated by scripts/sync-brand-assets.mjs. Do not edit the :root block by hand.',
      ' */',
      '',
    ].join('\n')
  }
  const css = readFileSync(cssPath, 'utf8')
  const rootIndex = css.indexOf(':root')
  if (rootIndex === -1) return css
  return css.slice(0, rootIndex).replace(/\s+$/, '\n\n')
}

/** Read tokens.css and return everything after the closing brace of `:root { ... }`. */
function preserveTrailer(cssPath) {
  if (!existsSync(cssPath)) return ''
  const css = readFileSync(cssPath, 'utf8')
  const rootIndex = css.indexOf(':root')
  if (rootIndex === -1) return ''
  // Find the matching closing brace of :root { ... }
  const open = css.indexOf('{', rootIndex)
  if (open === -1) return ''
  let depth = 0
  for (let i = open; i < css.length; i += 1) {
    const ch = css[i]
    if (ch === '{') depth += 1
    else if (ch === '}') {
      depth -= 1
      if (depth === 0) {
        return css.slice(i + 1).replace(/^\s+/, '\n\n')
      }
    }
  }
  return ''
}

function buildRootBlock(colors) {
  const lines = ['  /* generated from canshift-core DARK_TOKENS.colors — do not edit by hand */']
  for (const [key, hex] of Object.entries(colors)) {
    lines.push(`  --${camelToKebab(key)}: ${hex};`)
  }
  lines.push('')
  lines.push('  color-scheme: dark;')
  return `:root {\n${lines.join('\n')}\n}`
}

function copyAsset(from, to, label) {
  if (!existsSync(from)) {
    throw new Error(`Missing source asset for ${label}: ${from}`)
  }
  copyFileSync(from, to)
  console.log(`  copied ${label} → ${to}`)
}

function siblingsExist() {
  return existsSync(STUDIO_ROOT) && existsSync(CORE_ROOT)
}

function main() {
  if (!siblingsExist()) {
    console.error(
      'Sibling repos not found — clone canshift-studio and canshift-core next to this repo or skip this script.',
    )
    console.error(`  expected: ${STUDIO_ROOT}`)
    console.error(`  expected: ${CORE_ROOT}`)
    process.exit(1)
  }

  console.log('Syncing brand assets from canshift-studio + canshift-core...')

  copyAsset(STUDIO_LOGO, FLASHER_LOGO, 'logo')
  copyAsset(STUDIO_ICON, FLASHER_FAVICON, 'favicon')

  if (!existsSync(CORE_TOKENS)) {
    throw new Error(`Missing canshift-core design tokens at ${CORE_TOKENS}`)
  }
  const tokensSource = readFileSync(CORE_TOKENS, 'utf8')
  const colors = parseDarkTokensColors(tokensSource)

  const prelude = preservePrelude(FLASHER_TOKENS_CSS)
  const trailer = preserveTrailer(FLASHER_TOKENS_CSS)
  const rootBlock = buildRootBlock(colors)
  const next = `${prelude}${rootBlock}${trailer}`.replace(/\n{3,}/g, '\n\n').trimEnd() + '\n'
  writeFileSync(FLASHER_TOKENS_CSS, next, 'utf8')
  console.log(`  regenerated ${FLASHER_TOKENS_CSS} (${Object.keys(colors).length} color tokens)`)

  console.log('Done.')
}

try {
  main()
} catch (err) {
  console.error(err instanceof Error ? err.message : String(err))
  process.exit(1)
}
